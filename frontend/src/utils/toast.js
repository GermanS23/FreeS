import { toast } from 'react-toastify'

export const notifySuccess = (mensaje) => {
  toast.success(mensaje, {
    position: 'top-right',
    autoClose: 3000,
    hideProgressBar: false,
    closeOnClick: true,
    pauseOnHover: true,
    draggable: true,
  })
}

